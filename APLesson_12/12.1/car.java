public class car {
	private String paint, interior, engine, wheels;
    public car(String p, String i, String e, String w) {
        this.paint = p;
        this.interior = i;
        this.engine = e;
        this.wheels = w;
    }
    public String getPaint() {
        return paint;
    }
    public String getInterior() {
        return interior;
    }
    public String getEngine() {
        return engine;
    }
    public String getWheels() {
        return wheels;
    }
}
